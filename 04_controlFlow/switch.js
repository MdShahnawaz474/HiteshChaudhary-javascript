// Switch statement:-

// The switch statement is used to perform different actions based on different conditions.


const today= "monday"
switch (today) {
    case "monday":
        console.log("monday")
        break;
    case "tuesday":
        console.log("tuesday")
        break;

    default:
        console.log("wednesday")
        break;
}

