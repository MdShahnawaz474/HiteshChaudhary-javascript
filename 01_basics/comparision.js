//Arithmetic comparison
console.log(2>1);
console.log(2>=1);
console.log(2<1);
console.log(2==1);
console.log(2!=1);

//Datatype comparison

console.log("2">1);
console.log("02">1);

//Null comparison

console.log(null>0);
console.log(null==0);
console.log(null>=0);

//Undefined comparison

console.log(undefined==0);
console.log(undefined>0);
console.log(undefined>=0);
console.log(undefined<0)

//Strict comparison

console.log("02"===2);

